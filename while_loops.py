# Code practicing 'while' loops

# Using a for loop to iterate through values
for i in range(10):
    print('i is now {0}'.format(i))

# Using a while loop to iterate through values using a counter
print()
i = 0
while i - 1 < 10:
    print('i is now {0}'.format(i))
    i += 1

# In some languages the two above loops are computed the same way
# But in Python different things are happening behind the scenes
