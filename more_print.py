# Some practice exercises with using more of 'print()'s functions.

name = "Michael"
age = 35

# This print function is supplied with 4 objects for the '*objects' argument.
print(name, age, "Python", 2024)

# Here the 'sep=' argument has been specified to a comma and a space.
print(name, age, "Python, 2024", sep=", ")

